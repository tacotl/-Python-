"""
比萨 想出至少三种你喜欢的比萨，将其名称存储
在一个列表中，再使用for 循环将每种比萨的名称打印出来。
"""
# 修改这个for 循环，使其打印包含比萨名称的句子，而不仅仅是比萨的名称。对于每种比萨，都显示一行输出，下面是一个例子。
# I like pepperoni pizza.
pizzas = ['a','b','c']
for pizza in pizzas:
	print('I like pepperoni ' + pizza + '.')

# 在程序末尾添加一行代码，它不在for 循环中，指出你有多喜欢比萨。输出应包含针对每种比萨的消息，还有一个总结性句子，下面是一个例子。
# I really love pizza!
print('I really love pizza!')

"""
动物 想出至少三种有共同特征的动物，将其名称存储在一个列表中，再使用for 循环将每种动物的名称打印出来。
"""
animals = ['cow','sheep','horse']
for animal in animals:
	print(animal)

# 修改这个程序，使其针对每种动物都打印一个句子，下面是一个例子。
# A dog would make a great pet.
	print('A ' + animal +' would make a great pet.')

# 在程序末尾添加一行代码，指出这些动物的共同之处，如打印下面这样的句子。
# Any of these animals would make a great pet!
print('Any of these animals would make a great pet!')


